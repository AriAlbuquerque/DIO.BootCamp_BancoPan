    /**

     * Métodos são porções de código (sub-rotina) disponibilizados por uma classe;


   ***** O método sempre deverá estar dentro de uma CLASSE;

   ***** E será executado quando é requisitado(chamar um método),

   ***** Os métodos são rsponsáveis por definir e realizar um determinado comportamento nos softwares;

   ***** Tudo que for necessário realizar no programa será realizado por métodos em classes;

                 Método mínimo para criação de um método em JAVA:
                      nome(camelCase), (parâmetros[sempre obrigatório uso () contendo dado ou void;]), corpo(código que possui entre as {} ou void), retorno;

        ** O método void é utilizado quando a operaçção não precisa de retorno, apenas da sua funcionalidade;


*Assinatura -> Forma de identificar unicamente um método (Nome e Parâmetros());

*Mensagem ->Ato de chamar um método para que seja executado, dentro de uma classe ou objeto;

*Passagem -> de Parãmetros: Por valor(cópia) ou Referência(endereço);

*******Boas Práticas na criação de um método:
1-Nome descritivo, porém curtos; usar verbos e substantivos;
2-Padrão camelCase; e
3-Deve possuir entre 80 e 120 linhas;
4-Evite lista de parâmetros longas;
5-Visibilidade adequada;



        * Criação:

        nome_da_classe ou obj();
        nomde_do_método ou obj;
        parâmetros do método();


        * Sobre Carga:
        É a capacidade de definir métodos para diferentes contextos;
        Preservando o seu nome alterando apenas a sua lista de parâmetros;



        * Quadrilateros2:
        São instruções de interrupção(return) assim como break e continue;
        O retorno so será após a execução de método;
        Pode retornar um tipo primitivo, objeto ou exceçção;
        O return deve devolver o mesmo tipo do método;






    * */
