package QuartaSemana.EstruturaDeDados.Filas;

public class Resumo {
    /**
     * ************* FILA ************
     * FIFO (First in, First out);  Primeiro elemento que entra é o primeiro a sair;
     * Na estrutura de fila a referência do nó aponta por onde entra na fila, o primeiro FIRST sempre apontrá pra null;
     *
     * Métodos:
     *   .enqueue(); Enfileirar elementos; Introduz elemento e a Referência do nó apontará para ele;
     *   .dequeue(); Desinfileirar elemento; Retira o primeiro elemento da fila e seu anterior apontará para null;
     *   .isEmpty(); Testa se a fila está vazia;
     *
     * LIFO (Last in, First out); Ultimo elemento a entrar é o primeiro a sair;
     *
     */
}
